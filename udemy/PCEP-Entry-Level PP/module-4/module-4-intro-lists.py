cities = ['New York', 'Los Angeles', 'Chicago', 'Houston', 'Phoenix']
print(cities)
print(cities[0]) # gives the value
print(cities[-1]) # starts from the end
print(cities[-5])

print(cities[2:]) # On the right , it's inclusive and it gives a list as type
print(cities[:3]) # On the left , it's exclusive

print(cities[10:25]) # it's valid, no error thrown, give an empty list